// While loop
// While loop from 1 to 10
let i = 1;

while(i<=10){
    console.log(i)
    // We have to increment the value of i after every execution, so it can increase every time
    // otherwise the loop will never end
    i++
}

// While loop for even numbers (2,4,6,8)
let a = 2;
while(a<=10){
    console.log(a)
    // It will keep adding 2 in the current value of a
    a+=2
}

// Reverse loop 
// 10,9,8,7,6 . . . 1
let b = 10;
while(b>0){
    console.log(b);
    // It will keep decreasing the value of b by 1 every time
    b--;
}